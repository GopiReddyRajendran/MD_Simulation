% Title: Creation of single blue disc
% Author: Gopi Reddy Rajendran
% Description:
% This MATLAB code generates a single blue disc with x and y coordinates (0,0) using the scatter function.
% The scatter function is used to create a single blue disc, where x and y are the coordinates of the data points, and 1000 specifies the size of the markers, and 'b.' specifies the color and marker type of the markers (in this case, blue dots).
% Code:
% Clear the workspace and command window
clear;
clc;
% Initialize variables
x = 0;
y = 0;
% Generate scatter plot
scatter(x, y, 1000, 'b.');
% Add title and labels to the plot
title('Single Blue Disc');
% End of code
